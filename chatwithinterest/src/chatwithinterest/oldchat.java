package chatwithinterest;

public class oldchat {

    String mid;
    String msgfrom, msgto, message, msgtype, datatime;

    public oldchat(String mid, String msgfrom, String msgto, String message, String msgtype, String datatime) {
        this.mid = mid;
        this.msgfrom = msgfrom;
        this.msgto = msgto;
        this.message = message;
        this.msgtype = msgtype;
        this.datatime = datatime;
    }
    
    
}
